// --------------------------------------------------------------------------------
#include <Arduino.h>
#include "tft_handler.h"
#include "../logger/logger.h"
// --------------------------------------------------------------------------------
// Variables
// --------------------------------------------------------------------------------
MCUFRIEND_kbv tft;
// --------------------------------------------------------------------------------
void setupTft()
{
    tft.reset();

    uint16_t identifier = tft.readID(); // 9486
    identifier = 0x9486;

    tft.begin(identifier);
    pinMode(13, OUTPUT);
}
